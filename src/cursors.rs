use gtk::{gdk, gdk_pixbuf};

pub fn make_cursors() -> Vec<gdk::Cursor> {
    vec![
        make_cursor(CURSOR_UP_LEFT, 3, 3),
        make_cursor(CURSOR_UP, 10, 3),
        make_cursor(CURSOR_UP_RIGHT, 17, 3),
        make_cursor(CURSOR_LEFT, 3, 10),
        make_cursor(CURSOR_HOLD, 10, 10),
        make_cursor(CURSOR_RIGHT, 17, 10),
        make_cursor(CURSOR_DOWN_LEFT, 3, 17),
        make_cursor(CURSOR_DOWN, 10, 17),
        make_cursor(CURSOR_DOWN_RIGHT, 17, 17),
    ]
}

fn make_cursor(xpm: &[&str], hsx: i32, hsy: i32) -> gdk::Cursor {
    let pixbuf = gdk_pixbuf::Pixbuf::from_xpm_data(xpm).unwrap();
    let texture = gdk::Texture::for_pixbuf(&pixbuf);
    return gdk::Cursor::from_texture(&texture, hsx, hsy, None);
}

